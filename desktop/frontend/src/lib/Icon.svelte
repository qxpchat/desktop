<script lang="ts">
  type Props = {
    name: IconName;
    /** Pixel size; sets both width and height. Defaults to inheriting via 1em. */
    size?: number | string;
    /** Override stroke width. Lucide default is 2; we use 1.75 to match the iOS feel a little better. */
    stroke?: number;
    /** Optional aria-label — when omitted the icon is rendered as decorative. */
    label?: string;
  };

  let { name, size = '1em', stroke = 1.75, label }: Props = $props();

  let body = $derived(ICONS[name] ?? '');

  // Setting `innerHTML` directly on an `<svg>` element parses its children
  // in the SVG namespace, which is what we need. Svelte's `{@html}`
  // expression is documented as innerHTML-equivalent but in practice it has
  // sometimes injected children outside the SVG namespace (so rect/path/line
  // become unknown HTML elements with no rendering). Using an action that
  // sets `innerHTML` ourselves sidesteps that.
  function svgBody(node: SVGElement, html: string) {
    node.innerHTML = html;
    return {
      update(next: string) {
        if (next !== node.innerHTML) node.innerHTML = next;
      },
    };
  }
</script>

